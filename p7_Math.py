    # Functions related to math.

import math                                                 # Imports math module which contains a lot of math related functions.

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi))                                            # "round" rounds the input number to nearest integer.
print(math.ceil(pi))                                        # Uses ceil fuction from math module to round the input up.
print(math.floor(pi))                                       # As above but rounds the input down.
print(abs(pi))                                              # Gives absolute value of input. (Aka how far from 0 it is)
print(pow(pi,2))                                            # "pow" raises the input to a given power (2).
print(math.sqrt(pi))                                        # Finds the square root of input.
print(max(x,y,z))                                           # Finds highest value of inputs.
print(min(x,y,z))                                           # Finds lowest value of inputs.



